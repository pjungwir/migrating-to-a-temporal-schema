create extension if not exists btree_gist;
drop schema if exists temporal cascade;

create schema temporal;

/* work_categories */
CREATE TABLE temporal.work_categories (
  id integer GENERATED BY DEFAULT AS IDENTITY,
  valid_at daterange,
  client_id integer not null,
  name text not null,
  rate float not null,
  CONSTRAINT work_categories_pk PRIMARY KEY (id, valid_at WITHOUT OVERLAPS)
);
CREATE VIEW work_categories AS
  SELECT id, client_id, name, rate
  FROM temporal.work_categories
  WHERE valid_at @> current_date;

