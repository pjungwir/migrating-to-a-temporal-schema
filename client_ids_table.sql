create extension if not exists btree_gist;
drop schema if exists temporal cascade;
drop schema if exists nontemporal cascade;
drop schema if exists ids cascade;
create schema temporal;
create schema nontemporal;
create schema ids;

create table ids.clients (
  id integer generated by default as identity primary key
);
create table temporal.clients (
  id integer generated by default as identity,
  valid_at daterange,
  name text,
  constraint pk_clients primary key (id, valid_at without overlaps),
  constraint fk_clients_id foreign key (id) references ids.clients (id)
);
create table nontemporal.invoices (
  id integer generated by default as identity primary key,
  client_id integer not null references ids.clients (id),
  number integer not null
);

with new_ids as (
  insert into ids.clients DEFAULT VALUES returning id
)
insert into temporal.clients (id, valid_at, name)
  select id, daterange(current_date, null), 'Nadir'
  from new_ids;
  
insert into nontemporal.invoices (client_id, number)
  select max(id), 1001 from ids.clients;

select * from temporal.clients;
select * from nontemporal.invoices;

