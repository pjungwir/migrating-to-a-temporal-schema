-- WITH TRIGGERS:

/*                                                                                                 
 * Generate an ID for temporal.clients in temporal.single_clients                                  
 * so that non-temporal tables can have a foreign key reference to it.                             
 *                                                                                                 
 * Note this executes *before* temporal.clients own id column gets filled in by the GENERATED expression.                                                                                              * So if the is NULL, get an id from single_clients.                                               
 * Otherwise use the given id.                                                                     
 * (It's probably in single_clients already, but maybe not.)                                       
 */                                                                                                
CREATE OR REPLACE FUNCTION trg_generate_temporal_client_id()                                       
RETURNS trigger                                                                                    
LANGUAGE plpgsql                                                                                   
AS                                                                                                 
$$                                                                                                 
BEGIN                                                                                              
  IF NEW.id IS NULL THEN                                                                           
    NEW.id := nextval(pg_get_serial_sequence('temporal.clients', 'id'));                           
  END IF;                                                                                          
  INSERT INTO ids.clients (id) VALUES (NEW.id)                                         
    ON CONFLICT DO NOTHING;                                                                        
  RETURN NEW;                                                                                      
END;                                                                                               $$;

CREATE TRIGGER generate_temporal_client_id                                                         
  BEFORE INSERT ON temporal.clients                                                                
  FOR EACH ROW                                                                                     
  EXECUTE FUNCTION trg_generate_temporal_client_id();

